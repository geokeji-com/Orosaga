import { execFileSync } from 'node:child_process'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const parentToken = 'MmcRw3MgZiJwqIkTA15clNDpnvg'
const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const sleepBuffer = new Int32Array(new SharedArrayBuffer(4))

function sleep(milliseconds) {
  Atomics.wait(sleepBuffer, 0, 0, milliseconds)
}

function callLark(args, attempts = 4) {
  let lastError

  for (let attempt = 1; attempt <= attempts; attempt += 1) {
    try {
      const output = execFileSync('lark-cli', args, { encoding: 'utf8' })
      const result = JSON.parse(output)
      if (result.code !== 0) throw new Error(result.msg || `Lark API error ${result.code}`)
      sleep(120)
      return result
    } catch (error) {
      lastError = error
      if (attempt < attempts) sleep(attempt * 600)
    }
  }

  throw lastError
}

const parent = callLark([
  'wiki', 'spaces', 'get_node', '--as', 'user',
  '--params', JSON.stringify({ token: parentToken }), '--format', 'json',
])

const node = parent.data?.node
if (!node?.space_id) throw new Error('Unable to resolve Feishu Wiki space')

function listWikiChildren(parentNodeToken) {
  const items = []
  let pageToken

  do {
    const params = {
      space_id: node.space_id,
      parent_node_token: parentNodeToken,
      page_size: 50,
    }
    if (pageToken) params.page_token = pageToken

    const response = callLark([
      'wiki', 'nodes', 'list', '--as', 'user',
      '--params', JSON.stringify(params), '--format', 'json',
    ])
    items.push(...(response.data?.items ?? []))
    pageToken = response.data?.has_more ? response.data.page_token : undefined
  } while (pageToken)

  return items
}

function countDescendantDocuments(parentNodeToken, visited = new Set()) {
  if (visited.has(parentNodeToken)) return 0
  visited.add(parentNodeToken)

  const items = listWikiChildren(parentNodeToken)
  return items.reduce(
    (total, item) => total + 1 + (item.has_child ? countDescendantDocuments(item.node_token, visited) : 0),
    0,
  )
}

const children = listWikiChildren(parentToken)

const utilityTitles = new Set(['AI概念', 'GEO知识库个人分享访问量汇总'])
const camps = children
  .filter((item) => !utilityTitles.has(item.title))
  .map((item, index) => ({
    id: `CAMP-${String(index + 1).padStart(2, '0')}`,
    title: item.title,
    href: item.url || `https://wanhuxian.feishu.cn/wiki/${item.node_token}`,
    nodeToken: item.node_token,
    documentCount: countDescendantDocuments(item.node_token),
  }))
  .sort((left, right) => left.documentCount - right.documentCount || left.title.localeCompare(right.title, 'zh-CN'))

const target = resolve(root, 'src/camps.generated.ts')
mkdirSync(dirname(target), { recursive: true })
writeFileSync(
  target,
  `// Generated by npm run sync:camps. Do not edit manually.\nexport const camps = ${JSON.stringify(camps, null, 2)} as const\n`,
)

console.log(`Synced ${camps.length} camps from Feishu Wiki: ${node.title}`)
camps.forEach((camp) => console.log(`${camp.documentCount.toString().padStart(3, ' ')} docs  ${camp.title}`))
